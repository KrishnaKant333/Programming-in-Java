package com.codewithharry.shape;
public class Shape {
    int dim1, dim2, dim3;
    Shape(int dim1, int dim2, int dim3){
        this.dim1 = dim1;
        this.dim2 = dim2;
        this.dim3 = dim3;
    }
    public int getDim1(){
        return this.getDim1();
    }
    public int getDim2(){
        return this.getDim2();
    }
    public int getDim3(){
        return this.getDim3();
    }
}
