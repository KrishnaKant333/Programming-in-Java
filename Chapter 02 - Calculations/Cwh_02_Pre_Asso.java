public class Cwh_02_Pre_Asso {
    public static void main(String[] args) {
        int a = 6*5 - 34/2;
        int b = 60/5 - 34*2;
        System.out.println(a);
        System.out.println(b);

        int x = 2, y = 3;
        int z = x * x + y * y + 2 * x * y;
        int u = 4, v = 10, w = 1;
        int s = (v*v - u*u)/(2*w);
        System.out.println(z);
        System.out.println(s);
    }
}
